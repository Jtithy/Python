city = 'Bandarban, Bangladesh'
print(len(city)) #String length
print(city.upper()) #Convert to uppercase
print(city.lower()) #Convert to lowercase
print(city.title()) #Convert to title case
print(city.find('Bandarban')) #Find the index of a substring
print(city.replace('Bandarban', 'Chittagong')) #Replace a substring
print('Bandarban' in city) #Check if substring exists
